		<font face="ELEGANT TYPEWRITER"><p align="justify">
Majalengka “Remaja”, Religius, Maju dan Sejahtera dapat diwujudkan melalui Sumber Daya Manusia (SDM) yang handal dan Profesional.
Salah  satu  lembaga  yang kompeten melahirkan SDM pendukung suksesnya visi tersebut adalah SMK Negeri 1 Maja.
Sekolah   Menengah   Kejuruan   (SMK)   Negeri  3  Majalengka yang    dikukuhkan dengan SK Bupati No. 422/Kep.613-Dis Pk/2002. Sekolah yang dirintis dan berdiri sejak tahun 1968 dengan nama SPMA Perjuangan. Di tahun 1974 Status SPMA diakui.
Setelah diberlakukannya Otonomi Daerah SPMA Maja Naik statusnya   menjadi   “Negeri”   dengan   nama   Sekolah Menengah Kejuruan Negeri 3 Majalengka (SMKN 3) di bawah naungan Dinas Pendidikan Nasional.
Nama SMK Negeri 3 Majalengka pada tanggal 03 Maret 2008 berubah nama kembali menjadi SMK Negeri 1 Maja. Kini SMK Negeri 1 Maja telah terakreditasi A.</p>
<br>
<h3 style="font-family: Bohemian typewriter">Visi Misi</h3>
<p><b>Visi</b></p>
<p>Terwujudnya lulusan SMK Negeri 1 Maja yang cerdas, kompetitif dan berkarakter kuat</p>
<p><b>Misi</b></p><p>
1.	Mengembangkan kurikulum sekolah secara berkelanjutan;<br>
2.	Mengembangkan proses pembelajaran dan pengembangan diri secara profesional;<br>
3.	Mengembangkan sistem penilaian pendidikan berbasi kelas dan kompetensi;<br>
4.	Meningkatkan kinerja pendidik dan tenaga kependidikan yang profesional;<br>
5.	Mengembangkan sarana dan prasarana pendidikan;<br>
6.	Mengembangkan sekolah berbudaya lingkungan;<br>
7.	Mengembangkan kemitraan sekolah dengan berbagai pihak baik ke dalam maupun ke luar lembaga;<br>
8.	Menerapkan manajemen berbasis sekolah dan sistem manajemen mutu ISO 9001:2008;<br>
9.	Membangun jiwa peserta didik yang kreatif, inovatif,  sportif dan wirausaha; dan
10.	Meningkatkan pengawasan dan evaluasi program pendidikan;<br> </p>
<p><b>Tujuan</b></p>
<p><b>Untuk membentuk perserta didik menjadi insan yang :</b><br>
1.	Beriman dan bertaqwa kepada Tuhan Yang Maha Esa, berakhlak mulia, adil dan berkperidian luhur;<br>
2.	Cerdas, berilmu, disiplin, kerja keras, kritis, kreatif, motivatif, produktif dan gemar membaca;<br>
3.	Bersahabat, cinta damai, toleran, peduli sosial, peduli lingkungan, demokratis, semangat kebangsaan, cinta tanah air dan bertanggung jawab;
<br>
4.	Memiliki fisik dan mental sehat, bugar, berdaya tahan, sigap, terampil, trengginas, siap kerja, dan percaya diri untuk hidup mandiri di masyarakat, mengisi lowongan kerja, berwirausaha dan atau melanjutkan pendidikan ke jenjang pendidikan tinggi. </p>



